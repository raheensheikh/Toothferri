<script>
const rangeSlider = document.getElementById('range-slider');
  const circleValue = document.getElementById('circle-value');

  rangeSlider.addEventListener('input', updateCircle);

  function updateCircle() {
    const value = rangeSlider.value;
    const position = ((value - rangeSlider.min) / (rangeSlider.max - rangeSlider.min)) * 100;
    
    circleValue.textContent = value;
    const circle = document.getElementById('circle');
    circle.style.left = `calc(${position}% - 10px)`;
  }

  // Initial setup
  updateCircle();
</script>

